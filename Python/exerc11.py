# 11) Construir um algoritmo para ler 6 valores reais,
# calcular e imprimir a média desses valores.

num1 = float(input("Digite o primeiro número real: "))
num2 = float(input("Digite o segundo número real: "))
num3 = float(input("Digite o terceiro número real: "))
num4 = float(input("Digite o quarto número real: "))
num5 = float(input("Digite o quinto número real: "))
num6 = float(input("Digite o sexto número real: "))
media = (num1 + num2 + num3 + num4 + num5 + num6)/6
print("A média é ",media,".",sep="")
