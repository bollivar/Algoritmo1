#14) Faça um algoritmo onde deverá ser informado a base e a altura
# de um triângulo. O algoritmo deverá exibir o valor da área.

base = float(input("Informe a base do triângulo: "))
altura = float(input("Informe a altura do triângulo: "))
area = (base * altura)/2
print("A área do triângulo é ",area,".",sep="")
